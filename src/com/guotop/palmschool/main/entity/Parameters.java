package com.guotop.palmschool.main.entity;

/**
 * 首页第一排左边的各项参数
 * @author zhou
 *
 */
public class Parameters
{
	/**
	 * 未读消息
	 */
	private Integer unReadMessage;
	
	/**
	 * 最新留言
	 */
	private Integer newMessage;
	
	/**
	 * 迟到早退次数
	 */
	private Integer abnormalCount;
	
	/**
	 * 老师考勤异常
	 */
	private Integer teacherAbnormal;
	
	/**
	 * 学生考勤异常
	 */
	private Integer studentAbnormal;
	
	/**
	 * 请假审批
	 */
	private Integer leaveAudit;
	

	public Integer getUnReadMessage()
	{
		return unReadMessage;
	}

	public void setUnReadMessage(Integer unReadMessage)
	{
		this.unReadMessage = unReadMessage;
	}

	public Integer getNewMessage()
	{
		return newMessage;
	}

	public void setNewMessage(Integer newMessage)
	{
		this.newMessage = newMessage;
	}

	public Integer getAbnormalCount()
	{
		return abnormalCount;
	}

	public void setAbnormalCount(Integer abnormalCount)
	{
		this.abnormalCount = abnormalCount;
	}
	
	public Integer getTeacherAbnormal()
	{
		return teacherAbnormal;
	}

	public void setTeacherAbnormal(Integer teacherAbnormal)
	{
		this.teacherAbnormal = teacherAbnormal;
	}

	public Integer getStudentAbnormal()
	{
		return studentAbnormal;
	}

	public void setStudentAbnormal(Integer studentAbnormal)
	{
		this.studentAbnormal = studentAbnormal;
	}

	public Integer getLeaveAudit()
	{
		return leaveAudit;
	}

	public void setLeaveAudit(Integer leaveAudit)
	{
		this.leaveAudit = leaveAudit;
	}
}
