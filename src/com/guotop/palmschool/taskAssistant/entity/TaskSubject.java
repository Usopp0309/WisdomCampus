package com.guotop.palmschool.taskAssistant.entity;


public class TaskSubject {
	
	
	/**
	 * ID
	 */
	private Integer taskSubjectId;
	
	/**
	 * 科目名称
	 */
	private String subjectName;

	public Integer getTaskSubjectId()
	{
		return taskSubjectId;
	}

	public void setTaskSubjectId(Integer taskSubjectId)
	{
		this.taskSubjectId = taskSubjectId;
	}

	public String getSubjectName() {
		return subjectName;
	}

	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}

}
