package com.guotop.palmschool.taskAssistant.entity;

public class TaskHistory
{

	/**
	 * ID
	 */
	private Integer historyId;

	/**
	 * 科目ID
	 */
	private Integer taskSubjectId;
	
	/**
	 * 科目名称
	 */
	private String taskSubjectName;

	/**
	 * 科目作业内容
	 */
	private String taskContent;

	/**
	 * 科目作业内容图片
	 */
	private String taskContentImage;

	/**
	 * 作业发布时间
	 */
	private String createDate;

	/**
	 * 班级Id
	 */
	private Integer clazzId;
	/**
	 * 作业发送人Id
	 */
	private Integer sendUserId;

	/**
	 * 作业发送人姓名
	 */
	private String sendUserName;

	/**
	 * 创建时间
	 */
	private String createTime;

	public Integer getHistoryId()
	{
		return historyId;
	}

	public void setHistoryId(Integer historyId)
	{
		this.historyId = historyId;
	}

	public Integer getTaskSubjectId()
	{
		return taskSubjectId;
	}

	public void setTaskSubjectId(Integer taskSubjectId)
	{
		this.taskSubjectId = taskSubjectId;
	}

	public String getTaskContent()
	{
		return taskContent;
	}

	public void setTaskContent(String taskContent)
	{
		this.taskContent = taskContent;
	}

	public String getCreateDate()
	{
		return createDate;
	}

	public void setCreateDate(String createDate)
	{
		this.createDate = createDate;
	}

	public Integer getClazzId()
	{
		return clazzId;
	}

	public void setClazzId(Integer clazzId)
	{
		this.clazzId = clazzId;
	}

	public Integer getSendUserId()
	{
		return sendUserId;
	}

	public void setSendUserId(Integer sendUserId)
	{
		this.sendUserId = sendUserId;
	}

	public String getTaskContentImage()
	{
		return taskContentImage;
	}

	public void setTaskContentImage(String taskContentImage)
	{
		this.taskContentImage = taskContentImage;
	}

	public String getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}

	public String getSendUserName()
	{
		return sendUserName;
	}

	public void setSendUserName(String sendUserName)
	{
		this.sendUserName = sendUserName;
	}

	public String getTaskSubjectName()
	{
		return taskSubjectName;
	}

	public void setTaskSubjectName(String taskSubjectName)
	{
		this.taskSubjectName = taskSubjectName;
	}

}
