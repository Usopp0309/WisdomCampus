package com.guotop.palmschool.meeting.entity;

/**
 * 会议实体
 * 
 * @author
 */

public class MeetingDetail
{

	/**
	 * ID
	 */
	private Integer id;

	/**
	 * 会议名称
	 */
	private String meetingName;

	/**
	 * 会议内容
	 */
	private String content;

	/**
	 * 参会人ID
	 */
	private Integer attendeeId;

	/**
	 * 参会人
	 */
	private String attendee;

	/**
	 * 出席状态 0 正常 1迟到 2 早退 3 缺勤
	 */
	private Integer attenStatus;

	/**
	 * 会议时间
	 */
	private String meetingTime;

	/**
	 * 会议地点id
	 */
	private String meetingPlace;

	/**
	 * 会议地点
	 */
	private String meetingPlaceName;

	/**
	 * 会议id
	 */
	private Integer meetingId;

	/**
	 * 创建时间
	 */
	private String createTime;

	/**
	 * 部门名称
	 */
	private String departmentName;

	/**
	 * 工号
	 */
	private String code;

	/**
	 * ----------自定义属性-----------
	 */
	// 会议开始时间
	private String startTime;
	// 会议结束时间
	private String endTime;
	// 用作签到功能是否显示 0：可以签到 1不可以签到
	private Integer status;
	// 签到类型0:刷卡 1：wifi
	private Integer inoutType;

	// getters & setters ------------------------

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public String getMeetingName()
	{
		return meetingName;
	}

	public void setMeetingName(String meetingName)
	{
		this.meetingName = meetingName;
	}

	public String getContent()
	{
		return content;
	}

	public void setContent(String content)
	{
		this.content = content;
	}

	public Integer getAttendeeId()
	{
		return attendeeId;
	}

	public void setAttendeeId(Integer attendeeId)
	{
		this.attendeeId = attendeeId;
	}

	public Integer getAttenStatus()
	{
		return attenStatus;
	}

	public void setAttenStatus(Integer attenStatus)
	{
		this.attenStatus = attenStatus;
	}

	public Integer getMeetingId()
	{
		return meetingId;
	}

	public void setMeetingId(Integer meetingId)
	{
		this.meetingId = meetingId;
	}

	public String getAttendee()
	{
		return attendee;
	}

	public void setAttendee(String attendee)
	{
		this.attendee = attendee;
	}

	public String getMeetingTime()
	{
		return meetingTime;
	}

	public void setMeetingTime(String meetingTime)
	{
		this.meetingTime = meetingTime;
	}

	public String getMeetingPlace()
	{
		return meetingPlace;
	}

	public void setMeetingPlace(String meetingPlace)
	{
		this.meetingPlace = meetingPlace;
	}

	public String getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}

	public String getMeetingPlaceName()
	{
		return meetingPlaceName;
	}

	public void setMeetingPlaceName(String meetingPlaceName)
	{
		this.meetingPlaceName = meetingPlaceName;
	}

	public String getDepartmentName()
	{
		return departmentName;
	}

	public void setDepartmentName(String departmentName)
	{
		this.departmentName = departmentName;
	}

	public String getCode()
	{
		return code;
	}

	public void setCode(String code)
	{
		this.code = code;
	}

	public String getStartTime()
	{
		return startTime;
	}

	public void setStartTime(String startTime)
	{
		this.startTime = startTime;
	}

	public String getEndTime()
	{
		return endTime;
	}

	public void setEndTime(String endTime)
	{
		this.endTime = endTime;
	}

	public Integer getStatus()
	{
		return status;
	}

	public void setStatus(Integer status)
	{
		this.status = status;
	}

	public Integer getInoutType()
	{
		return inoutType;
	}

	public void setInoutType(Integer inoutType)
	{
		this.inoutType = inoutType;
	}

}
