package com.guotop.palmschool.entity;

public class Sms
{
	/**
	 * ID
	 */
	private Integer id;

	/**
	 * 短信内容
	 */
	private String content;

	/**
	 * 发送人
	 */
	private Integer senderId;

	/**
	 * 发送人姓名
	 */
	private String senderName;

	/**
	 * 模块
	 */
	private Integer module;

	/**
	 * 短信长度
	 */
	private Integer smsLength;

	/**
	 * 发送手机号个数(对应单用户多手机)
	 */
	private Integer phoneCount;

	/**
	 * 状态：<br>
	 * 0: 待发送 <br>
	 * 1: 发送成功 <br>
	 * 2: 发送失败 <br>
	 * 3: 部分成功 <br>
	 * 4: 无接收人 <br>
	 * 5: 内容有误 <br>
	 * 99: 本地测试
	 */
	private Integer result;

	/**
	 * 返回状态报告数(短信接口回执)
	 */
	private Integer reportAmount;

	/**
	 * 成功接收数(短信接口回执)
	 */
	private Integer succAmount;

	/**
	 * 发送时间
	 */
	private String sentTime;

	/**
	 * 创建时间
	 */
	private String createTime;

	/**
	 * 是否实时发送 0：定时发送 1：实时发送
	 */
	private String isRealTime;

	/**
	 * 短信发送用途
	 */
	private String type;
	
	/**
	 * 发送短信条数
	 */
	public Integer smsSum;
	
	/**
	 * 发送短信失败总条数
	 */
	public Integer errorSmsSum;
	
	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public String getContent()
	{
		return content;
	}

	public void setContent(String content)
	{
		this.content = content;
	}

	public Integer getSenderId()
	{
		return senderId;
	}

	public void setSenderId(Integer senderId)
	{
		this.senderId = senderId;
	}

	public String getSenderName()
	{
		return senderName;
	}

	public void setSenderName(String senderName)
	{
		this.senderName = senderName;
	}

	public Integer getModule()
	{
		return module;
	}

	public void setModule(Integer module)
	{
		this.module = module;
	}

	public Integer getSmsLength()
	{
		return smsLength;
	}

	public void setSmsLength(Integer smsLength)
	{
		this.smsLength = smsLength;
	}

	public Integer getPhoneCount()
	{
		return phoneCount;
	}

	public void setPhoneCount(Integer phoneCount)
	{
		this.phoneCount = phoneCount;
	}

	public Integer getResult()
	{
		return result;
	}

	public void setResult(Integer result)
	{
		this.result = result;
	}

	public Integer getReportAmount()
	{
		return reportAmount;
	}

	public void setReportAmount(Integer reportAmount)
	{
		this.reportAmount = reportAmount;
	}

	public Integer getSuccAmount()
	{
		return succAmount;
	}

	public void setSuccAmount(Integer succAmount)
	{
		this.succAmount = succAmount;
	}

	public String getSentTime()
	{
		return sentTime;
	}

	public void setSentTime(String sentTime)
	{
		this.sentTime = sentTime;
	}

	public String getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}

	public String getIsRealTime()
	{
		return isRealTime;
	}

	public void setIsRealTime(String isRealTime)
	{
		this.isRealTime = isRealTime;
	}

	public String getType()
	{
		return type;
	}

	public void setType(String type)
	{
		this.type = type;
	}

	public Integer getSmsSum()
	{
		return smsSum;
	}

	public void setSmsSum(Integer smsSum)
	{
		this.smsSum = smsSum;
	}

	public Integer getErrorSmsSum()
	{
		return errorSmsSum;
	}

	public void setErrorSmsSum(Integer errorSmsSum)
	{
		this.errorSmsSum = errorSmsSum;
	}

}
