package com.guotop.palmschool.entity;

public class Groupmember
{
	private int id;
	private int userId;
	private int groupId;
	private String showName;
	private String phone;
	private int type;
	private int modifiable;
	private String createtime;
	private String activetime;
	private String myGroupCard;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public int getUserId()
	{
		return userId;
	}

	public void setUserId(int userId)
	{
		this.userId = userId;
	}

	public int getGroupId()
	{
		return groupId;
	}

	public void setGroupId(int groupId)
	{
		this.groupId = groupId;
	}

	public String getShowName()
	{
		return showName;
	}

	public void setShowName(String showName)
	{
		this.showName = showName;
	}

	public String getPhone()
	{
		return phone;
	}

	public void setPhone(String phone)
	{
		this.phone = phone;
	}

	public int getType()
	{
		return type;
	}

	public void setType(int type)
	{
		this.type = type;
	}

	public int getModifiable()
	{
		return modifiable;
	}

	public void setModifiable(int modifiable)
	{
		this.modifiable = modifiable;
	}

	public String getCreatetime()
	{
		return createtime;
	}

	public void setCreatetime(String createtime)
	{
		this.createtime = createtime;
	}

	public String getActivetime()
	{
		return activetime;
	}

	public void setActivetime(String activetime)
	{
		this.activetime = activetime;
	}

	public String getMyGroupCard()
	{
		return myGroupCard;
	}

	public void setMyGroupCard(String myGroupCard)
	{
		this.myGroupCard = myGroupCard;
	}
}