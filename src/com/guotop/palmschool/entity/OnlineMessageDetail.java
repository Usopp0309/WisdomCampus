package com.guotop.palmschool.entity;

public class OnlineMessageDetail
{
	/**
	 * ID
	 */
	private Integer id;

	/**
	 * 短信内容
	 */
	private String content;
	/**
	 * 发送人id
	 * 
	 */
	private Integer senderId;

	/**
	 * 发送人
	 * 
	 */
	private String senderName;

	/**
	 * 接受人id
	 */
	private Integer receiverId;

	/**
	 * 接受人名字
	 */
	private String receiverName;

	/**
	 * 留言状态
	 */
	private Integer status;

	/**
	 * 
	 */
	private Integer onlineMessageId;

	/**
	 * 创建时间
	 */
	private String createTime;

	/**
	 * 发送时间
	 */
	private String sentTime;

	/**
	 * 间隔时长
	 * 
	 */
	private String timeInterval;

	/**
	 * 所有未读消息的数量
	 * 
	 * @return
	 */
	private Integer notReadOnlineMessageCount;

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public String getContent()
	{
		return content;
	}

	public void setContent(String content)
	{
		this.content = content;
	}

	public Integer getSenderId()
	{
		return senderId;
	}

	public void setSenderId(Integer senderId)
	{
		this.senderId = senderId;
	}

	public String getSenderName()
	{
		return senderName;
	}

	public void setSenderName(String senderName)
	{
		this.senderName = senderName;
	}

	public Integer getReceiverId()
	{
		return receiverId;
	}

	public void setReceiverId(Integer receiverId)
	{
		this.receiverId = receiverId;
	}

	public String getReceiverName()
	{
		return receiverName;
	}

	public void setReceiverName(String receiverName)
	{
		this.receiverName = receiverName;
	}

	public Integer getStatus()
	{
		return status;
	}

	public void setStatus(Integer status)
	{
		this.status = status;
	}

	public Integer getOnlineMessageId()
	{
		return onlineMessageId;
	}

	public void setOnlineMessageId(Integer onlineMessageId)
	{
		this.onlineMessageId = onlineMessageId;
	}

	public String getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}

	public String getTimeInterval()
	{
		return timeInterval;
	}

	public void setTimeInterval(String timeInterval)
	{
		this.timeInterval = timeInterval;
	}

	public Integer getNotReadOnlineMessageCount()
	{
		return notReadOnlineMessageCount;
	}

	public void setNotReadOnlineMessageCount(Integer notReadOnlineMessageCount)
	{
		this.notReadOnlineMessageCount = notReadOnlineMessageCount;
	}

	public String getSentTime()
	{
		return sentTime;
	}

	public void setSentTime(String sentTime)
	{
		this.sentTime = sentTime;
	}

}
