package com.guotop.palmschool.entity;

import java.io.Serializable;

public class UserPermission implements Serializable
{
	/**
	 * 
	 */
	private static final long serialVersionUID = 5166585646512935557L;
	private Integer id;
	// 用户ID
	private Integer userId;
	// 权限编码
	private String permissionCode;
	// 待操作班级ID
	private String optClazzId;
	// 待操作部门ID
	private String optDepartId;
	// 权限对应的roleCode
	private String roleCode;

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public Integer getUserId()
	{
		return userId;
	}

	public void setUserId(Integer userId)
	{
		this.userId = userId;
	}

	public String getPermissionCode()
	{
		return permissionCode;
	}

	public void setPermissionCode(String permissionCode)
	{
		this.permissionCode = permissionCode;
	}

	public String getOptClazzId()
	{
		return optClazzId;
	}

	public void setOptClazzId(String optClazzId)
	{
		this.optClazzId = optClazzId;
	}

	public String getOptDepartId()
	{
		return optDepartId;
	}

	public void setOptDepartId(String optDepartId)
	{
		this.optDepartId = optDepartId;
	}

	public String getRoleCode()
	{
		return roleCode;
	}

	public void setRoleCode(String roleCode)
	{
		this.roleCode = roleCode;
	}

}
