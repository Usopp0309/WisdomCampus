package com.guotop.palmschool.entity;

import java.util.ArrayList;
import java.util.List;


/**
 * 未购买服务费对应的菜单限制表
 * 
 */
public class OrderMessagePermission 
{
	/**
	 * 菜单Id
	 */
	private String menuId;
	
	/**
	 * 菜单名称
	 */
	private String menuName;
	
	/**
	 * 未缴纳服务费是否限制功能使用 0不限制 1限制
	 */
	private Integer status;
	
	/**
	 * 限制的年级列表
	 */
	private List<OrderMessagePermissionDetail> detailList = new ArrayList<OrderMessagePermissionDetail>();

	public String getMenuId()
	{
		return menuId;
	}

	public void setMenuId(String menuId)
	{
		this.menuId = menuId;
	}

	public String getMenuName()
	{
		return menuName;
	}

	public void setMenuName(String menuName)
	{
		this.menuName = menuName;
	}

	public Integer getStatus()
	{
		return status;
	}

	public void setStatus(Integer status)
	{
		this.status = status;
	}

	public List<OrderMessagePermissionDetail> getDetailList()
	{
		return detailList;
	}

	public void setDetailList(List<OrderMessagePermissionDetail> detailList)
	{
		this.detailList = detailList;
	}

}
