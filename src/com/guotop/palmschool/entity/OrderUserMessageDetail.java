package com.guotop.palmschool.entity;

/**
 * 用户购买短信套餐信息使用详情实体类
 * 
 */
public class OrderUserMessageDetail
{
	/**
	 * ID
	 */
	private Integer id;

	/**
	 * 购买人Id
	 */
	private Integer userId;

	/**
	 * 类型 0包时间段 1 单价/每条
	 */
	private Integer type;

	/**
	 * 本次短信条数
	 */
	private Integer count;

	/**
	 * 原剩余条说
	 */
	private Integer orgCount;

	/**
	 * 现剩余条数
	 */
	private Integer nowCount;

	/**
	 * 对应的套餐id
	 */
	private Integer messageId;

	/**
	 * 短信套餐用户购买Id
	 */
	private Integer messUserId;

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id = id;
	}

	public Integer getUserId()
	{
		return userId;
	}

	public void setUserId(Integer userId)
	{
		this.userId = userId;
	}

	public Integer getType()
	{
		return type;
	}

	public void setType(Integer type)
	{
		this.type = type;
	}

	public Integer getCount()
	{
		return count;
	}

	public void setCount(Integer count)
	{
		this.count = count;
	}

	public Integer getOrgCount()
	{
		return orgCount;
	}

	public void setOrgCount(Integer orgCount)
	{
		this.orgCount = orgCount;
	}

	public Integer getNowCount()
	{
		return nowCount;
	}

	public void setNowCount(Integer nowCount)
	{
		this.nowCount = nowCount;
	}

	public Integer getMessageId()
	{
		return messageId;
	}

	public void setMessageId(Integer messageId)
	{
		this.messageId = messageId;
	}

	public Integer getMessUserId()
	{
		return messUserId;
	}

	public void setMessUserId(Integer messUserId)
	{
		this.messUserId = messUserId;
	}

}
