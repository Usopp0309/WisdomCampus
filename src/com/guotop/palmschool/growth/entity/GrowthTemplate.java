package com.guotop.palmschool.growth.entity;

public class GrowthTemplate
{
	//ID
	private int id;
	//学生姓名
	private String name;
	//路径
	private String path;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getPath()
	{
		return path;
	}
	public void setPath(String path)
	{
		this.path = path;
	}
	
}
