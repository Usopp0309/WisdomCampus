package com.guotop.palmschool.rest.entity;
/**
 * 基站实体类
 * @author jfy
 * @date 2016年1月28日
 */
public class WifiInfo
{
	private String id;
	private String posId;
	private String wifiName; // 区域码
	private String macAddr; // 基站编号
	private String signalStreng; // 信号强度

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPosId() {
		return posId;
	}

	public void setPosId(String posId) {
		this.posId = posId;
	}

	public String getWifiName() {
		return wifiName;
	}

	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}

	public String getMacAddr() {
		return macAddr;
	}

	public void setMacAddr(String macAddr) {
		this.macAddr = macAddr;
	}

	public String getSignalStreng() {
		return signalStreng;
	}

	public void setSignalStreng(String signalStreng) {
		this.signalStreng = signalStreng;
	}
	
}
