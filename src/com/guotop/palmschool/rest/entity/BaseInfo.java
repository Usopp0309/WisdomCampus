package com.guotop.palmschool.rest.entity;
/**
 * 基站实体类
 * @author jfy
 * @date 2016年1月28日
 */
public class BaseInfo
{
	private String id;
	private String posId;
	private String areaCode; // 区域码
	private String baseStationNumber; // 基站编号
	private String signalStreng; // 信号强度

	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPosId() {
		return posId;
	}

	public void setPosId(String posId) {
		this.posId = posId;
	}

	public String getAreaCode() {
		return areaCode;
	}

	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}

	public String getBaseStationNumber() {
		return baseStationNumber;
	}

	public void setBaseStationNumber(String baseStationNumber) {
		this.baseStationNumber = baseStationNumber;
	}

	public String getSignalStreng() {
		return signalStreng;
	}

	public void setSignalStreng(String signalStreng) {
		this.signalStreng = signalStreng;
	}
	
}
