package com.guotop.palmschool.evaluation.entity;

import java.util.List;


public class EvaluationExercises {
	
	//主键Id
	private int id;
	
	
	//测试卷ID
	private Integer paperId;
	
	
	//嫡属于文件夹的ID
	private Integer exFolderId;
	
	//该题分数
	private Integer integral;
	
	//题目名称
	private String name;
	
	//题目内容类型
	private String contentType;
	
	//题目类型
	private String type;
	
	//题目创建时间
	private String createTime;
	
	//对该题目的说明
	private String comment;
	
	//更新时间
	private String updateTime;
	
	//当前状态
	private Integer status;
	
	//正确答案
	private String answer;
	
	private String html;
	
	//得分
	private Integer score;
	
	//题目详情
	private List<EvaluationExercisesDetail> detail;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public Integer getPaperId()
	{
		return paperId;
	}

	public void setPaperId(Integer paperId)
	{
		this.paperId = paperId;
	}

	public Integer getExFolderId()
	{
		return exFolderId;
	}

	public void setExFolderId(Integer exFolderId)
	{
		this.exFolderId = exFolderId;
	}

	public Integer getIntegral()
	{
		return integral;
	}

	public void setIntegral(Integer integral)
	{
		this.integral = integral;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getContentType()
	{
		return contentType;
	}

	public void setContentType(String contentType)
	{
		this.contentType = contentType;
	}

	public String getType()
	{
		return type;
	}

	public void setType(String type)
	{
		this.type = type;
	}

	public String getCreateTime()
	{
		return createTime;
	}

	public void setCreateTime(String createTime)
	{
		this.createTime = createTime;
	}

	public String getComment()
	{
		return comment;
	}

	public void setComment(String comment)
	{
		this.comment = comment;
	}

	public String getUpdateTime()
	{
		return updateTime;
	}

	public void setUpdateTime(String updateTime)
	{
		this.updateTime = updateTime;
	}

	public Integer getStatus()
	{
		return status;
	}

	public void setStatus(Integer status)
	{
		this.status = status;
	}

	public String getAnswer()
	{
		return answer;
	}

	public void setAnswer(String answer)
	{
		this.answer = answer;
	}

	public String getHtml()
	{
		return html;
	}

	public void setHtml(String html)
	{
		this.html = html;
	}

	public Integer getScore()
	{
		return score;
	}

	public void setScore(Integer score)
	{
		this.score = score;
	}
	
	public List<EvaluationExercisesDetail> getDetail() {
		return detail;
	}
	public void setDetail(List<EvaluationExercisesDetail> list) {
		this.detail =list;
	}

}
