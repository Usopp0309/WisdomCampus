package com.guotop.palmschool.bluetooth.entity;

/**
 * 手环使用课程时间
 * 
 * @author chenyong
 *
 */
public class BluetoothCourseTime {
	/**
	 * 课程主键
	 */
	private Integer bluetoothCourseId;
	/**
	 * 主键
	 */
	private Integer id;
	/**
	 * 开始时间
	 */
	private String startTime;
	/**
	 * 结束时间
	 */
	private String endTime;
	/**
	 * 创建人
	 */
	private Integer userId;
	/**
	 * 创建时间
	 */
	private String createTime;
	/**
	 * 课程名称
	 */
	private String courseName;
	/**
	 * 结束日期（HH:mm:ss）
	 */
    private String time;
    
	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public Integer getBluetoothCourseId() {
		return bluetoothCourseId;
	}

	public void setBluetoothCourseId(Integer bluetoothCourseId) {
		this.bluetoothCourseId = bluetoothCourseId;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

}
